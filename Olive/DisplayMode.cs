namespace Olive;

/// <summary>
///     An enumeration of display modes.
/// </summary>
public enum DisplayMode
{
    /// <summary>
    ///     Specifies that the display should be windowed.
    /// </summary>
    Windowed,

    /// <summary>
    ///     Specifies that the display should be fullscreen.
    /// </summary>
    Fullscreen,

    /// <summary>
    ///     Specifies that the display should be fullscreen without adjusting hardware mode.
    /// </summary>
    FullscreenBorderless
}
